try {
    // Код, который может вызвать ошибку
    const result = someFunction(); // Попытка вызвать функцию
    console.log(result); // Если функция выполняется успешно, выводим результат
  } catch (error) {
    // Если возникла ошибка, перехватываем её и обрабатываем
    console.error('Произошла ошибка:', error.message); // Выводим сообщение об ошибке
  }
// Код, который выполняется после блока try...catch
console.log('Этот код выполняется в любом случае');